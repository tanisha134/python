#Inheritance allows you to build new classes by reusing and extending the functionality of existing ones.

#Abstraction is  a programming concept that hides complex implementation details while exposing only essential information and functionalities to users.

#Encapsulation is a fundamental object-oriented principle in Python.
# It protects your classes from accidental changes or deletions and promotes code reusability and maintainability.

#Polymorphism is a term used to refer to an object's ability to take on multiple forms.




